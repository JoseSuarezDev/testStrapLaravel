    public function login(Request $request)
    {
        try {
            $User = User::where('email', $request->user['email'])->get();
            if (Hash::check($request->user["password"], $User[0]->password)) {
                return json_encode($User[0]);
            } else { throw new Exception('La clave no es la misma'); }
        } catch (Throwable $th) {
            return json_encode($th->getMessage());
        }
    }


